DAMAGE_LEVEL_IMAGES = {
    1: 101,
    2: 102,
    3: 103,
}

DAMAGE_LEVELS = len(DAMAGE_LEVEL_IMAGES)

EQ_ITEMS = [1, 2, 3]
EQ_ITEMS_COUNT = len(EQ_ITEMS)

def render_block(surface: pygame.Surface, position: tuple, block_id: int):
    if block_id == 0:
        return
    
    x = position[0]
    y = position[1]

    px_pos_x = BLOCK_SIZE * x
    px_pos_y = BLOCK_SIZE * y

    render_position = (px_pos_x, px_pos_y)
    surface.blit(images[block_id], render_position)
    
    # render breakage
    breakage = board_breakage[y][x]
    resistance = block_resistance[block_id]
    
    damage_level = math.ceil((breakage / resistance * DAMAGE_LEVELS))

    if damage_level > 0:
        surface.blit(images[DAMAGE_LEVEL_IMAGES[damage_level]], render_position)


def render(surface: pygame.Surface):
    for y, row in enumerate(board):
        for x, block_id in enumerate(row):
            render_block(surface, (x, y), block_id)


def is_right_button_pressed() -> bool:
    return pygame.mouse.get_pressed()[pygame.BUTTON_RIGHT-1]


def is_left_button_pressed() -> bool:
    return pygame.mouse.get_pressed()[pygame.BUTTON_LEFT-1]


def get_block_resistance(block_id: int) -> int:
    return block_resistance[block_id]


def block_destroy(coordinates: Tuple[int, int]) -> None:
    x = coordinates[0]
    y = coordinates[1]
    board[y][x] = BLOCK_NONE
    board_breakage[y][x] = 0


def block_tap(coordinates: Tuple[int, int]) -> None:
    x = coordinates[0]
    y = coordinates[1]
    block_id = board[y][x]

    if board_breakage[y][x] >= get_block_resistance(block_id):
        block_destroy(coordinates)
        return

    board_breakage[y][x] += 1


def on_block_clicked(coordinates: Tuple[int, int]) -> None:
    x = coordinates[0]
    y = coordinates[1]
    block: int = board[y][x]
    if is_right_button_pressed() and block != BLOCK_NONE:
        block_tap(coordinates)


def on_block_mouse_on(coordinates: Tuple[int, int]) -> None:
    x = coordinates[0]
    y = coordinates[1]
    block: int = board[y][x]
    if is_left_button_pressed() and block == BLOCK_NONE:
        board[y][x] = EQ_ITEMS[ui_selected_item]


pygame.init()

screen = pygame.display.set_mode([WINDOW_WIDTH, WINDOW_HEIGHT])
ui_selected_item = 0

running = True
while running:

    cursor_pos = pygame.mouse.get_pos()
    mouse_on_block_coordinates = get_board_element_pointed_by(cursor_pos, BLOCK_SIZE)

    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            running = False
        if event.type == pygame.MOUSEBUTTONDOWN:
            on_block_clicked(mouse_on_block_coordinates)
        if event.type == pygame.MOUSEWHEEL:
            ui_selected_item -= event.y
            ui_selected_item = max(0, ui_selected_item)
            ui_selected_item = min(EQ_ITEMS_COUNT-1, ui_selected_item)

    on_block_mouse_on(mouse_on_block_coordinates)

    screen.fill((255, 255, 255))

    # pygame.draw.circle(screen, (0, 0, 255), (250, 250), 75)
    render(screen)
    items_frame(EQ_ITEMS, screen, ui_selected_item)

    pygame.display.flip()

pygame.quit()