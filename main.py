def render(surface: pygame.Surface):
    cursor_pos = pygame.mouse.get_pos()
    pointed_block = get_board_element_pointed_by(cursor_pos, BLOCK_SIZE)
    for y, row in enumerate(board):
        for x, block_id in enumerate(row):
            color = None
            if block_id == 0:
                continue
            pox_x = BLOCK_SIZE * x
            pox_y = BLOCK_SIZE * y

            if pointed_block == (x, y):
                color = POINTED_BLOCK_COLOR

            render_block(surface, (pox_x, pox_y), block_id, color)


def on_block_clicked(coordinates: Tuple[int, int]) -> None:
    x = coordinates[0]
    y = coordinates[1]
    block: int = board[y][x]
    if block != BLOCK_NONE:
        board[y][x] = BLOCK_NONE
    else:
        board[y][x] = BLOCK_GRASS


screen = pygame.display.set_mode([800, 600])

running = True
while running:

    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            running = False
        if event.type == pygame.MOUSEBUTTONDOWN:
            cursor_pos = pygame.mouse.get_pos()
            coordinates = get_board_element_pointed_by(cursor_pos, BLOCK_SIZE)
            on_block_clicked(coordinates)

    screen.fill((255, 255, 255))

    # pygame.draw.circle(screen, (0, 0, 255), (250, 250), 75)
    render(screen)

    pygame.display.flip()

pygame.quit()